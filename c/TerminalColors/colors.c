//Generate macros for color terminals
// Allows you to change color of text displaying in terminal
//
//
//
// format: \x1b or \033  <-- Terminal Escape codes
// 	   [38;2         <-- 30-37 specify regular 16 bit colors 38;2; says to use terminal full range//
//	   Rval;Gval;Bval <-- RGB number values
//	   m               <-- Terminator
//
//	   \033[38;2;255;0;0m <--- Everything after this will be red!
//	   \033[38;2;0;255;0m <--- Everything after this will be green!
//	   \033[38;2;0;0;255m <--- Everything after this will be blue!
//
//




